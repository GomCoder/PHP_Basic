    $n = "label1";

    switch ($n) {
        case "label1":
            echo "n값이 label1일 경우 실행됩니다.";
            break;
        case "label2":
            echo "n값이 label2일 경우 실행됩니다.";
            break;
        case "label3":
            echo "n값이 label3일 경우 실행됩니다.";
            break;
        default:
            echo "n값이 일치하는 게 없는 경우 실행됩니다.";
    
    }
    

?>